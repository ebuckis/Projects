int		ft_count_if(char **tab, int (*f)(char*))
{
	int i;
	int length;

	i = 0;
	length = 0;
	while (tab[i][0])
	{
		if (f(tab[i]) != 0)
			length++;
		i++;
	}
	return (length);
}
